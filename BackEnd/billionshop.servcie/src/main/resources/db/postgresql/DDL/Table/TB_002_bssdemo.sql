COMMENT ON COLUMN billionshopdb.BSSDEMO.DEMOTESTDATA2 IS 'demotest data2';
COMMENT ON COLUMN billionshopdb.BSSDEMO.DEMOCREDAT IS 'Creation Date';
COMMENT ON COLUMN billionshopdb.BSSDEMO.DEMOCREUSR IS 'Creation User';
