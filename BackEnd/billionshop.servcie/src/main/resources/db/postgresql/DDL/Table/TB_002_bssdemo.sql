CREATE TABLE billionshop.BSSDEMO (
	DEMODEMOID      NUMERIC(4) NOT NULL,
	DEMOTESTDATA1   VARCHAR(255) NULL,
	DEMOTESTDATA2   VARCHAR(255) NULL,
	DEMOCREDAT      TIMESTAMP NOT NULL,
	DEMOCREUSR		VARCHAR(150) NOT NULL,
	DEMOUPDDAT      TIMESTAMP NOT NULL,
	DEMOUPDUSR		VARCHAR(150) NOT NULL
	CONSTRAINT bssdemo_pk PRIMARY KEY (demodemoid)
)
-- Column comments

